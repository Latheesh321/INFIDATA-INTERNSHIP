print("subtraction of {} and {} is {}".format(sri_number_input,kee_number_input,sub))

mul = sri_number_input * kee_number_input
print("multiplication of {} and {} is {}".format(sri_number_input,kee_number_input,mul))

div = sri_number_input / kee_number_input
print("division of {} and {} is {}".format(sri_number_input,kee_number_input,div))












